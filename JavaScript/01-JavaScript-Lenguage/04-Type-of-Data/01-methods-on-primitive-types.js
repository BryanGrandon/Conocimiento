let str1 = "Helllo";
// .toLowerCase()
let upperCase = str1.toUpperCase();
console.log(upperCase); // HELLO

// .toLowerCase()
let lowerCase = str1.toLowerCase();
console.log(lowerCase); // hello

// .toFixed()
let number = 1.23456;
console.log(number.toFixed(2)); // 1.23

// String to number
let num = Number("123");
console.log(num); // 123
