let suma = 2 + 4; // 6
suma += 2; // -> suma = suma + 2;
console.log(suma); // 8

let resta = 2 - 4; // -2
resta -= 4; // -> resta = resta - 4;
console.log(resta); // -6

let divicion = 2 / 4; // 0.5
divicion /= 2; // -> divicion = divicion / 2;
console.log(divicion); // 0.25

let multiplicacion = 2 * 4; // 8
multiplicacion *= 3; // -> multiplicacion = multiplicacion * 3;
console.log(multiplicacion); // 24

let elevacion = 2 ** 3; // 8
elevacion **= 2; // -> elevacion = elevacion ** 2;
console.log(elevacion); // 64

let modulo = 9 % 5; // 4 -> resto
modulo %= 2; // -> modulo = modulo % 2;
console.log(modulo); // 0

/* Incremento */

let x = 4;
x++; // -> x = x + 1;
console.log(x); // 5

/* Decremento */

let y = 6;
y--; // y = y - 1;
console.log(y); // 5
